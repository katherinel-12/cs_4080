bool compile(const char* source, Chunk* chunk) {
    initScanner(source);

    compilingChunk = chunk;
    parser.hadError = false;
    parser.panicMode = false;
    initTable(&stringConstants); // <--

    advance();

    while (!match(TOKEN_EOF)) {
        declaration();
    }

    endCompiler();
    freeTable(&stringConstants); // <--
    return !parser.hadError;
}